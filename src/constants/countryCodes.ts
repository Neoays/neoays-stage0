// Keep original list for backward compatibility
export const COUNTRY_CODES = ALL_COUNTRY_CODES;

export const getDefaultCountryCode = (merchantCountryCode?: string): string => {
    // If merchant has a country code, use it
    if (merchantCountryCode) {
        const found = COUNTRY_CODES.find(c => c.code === merchantCountryCode);
        if (found) return merchantCountryCode;
    }
    // Default to Oman
    return '+968';
};

export const getCountryByCode = (code: string) => {
    return COUNTRY_CODES.find(c => c.code === code) || COUNTRY_CODES[0];
};

// Search countries by name or code
export const searchCountries = (query: string) => {
    const lowerQuery = query.toLowerCase();
    return COUNTRY_CODES.filter(c =>
        c.name.toLowerCase().includes(lowerQuery) ||
        c.code.includes(query) ||
        c.country.toLowerCase().includes(lowerQuery)
    );
};
